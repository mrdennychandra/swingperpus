    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        //clear();
        String custName = txtCust.getText();
        String notaNumber = txtNota.getText();
        Date date = new Date();
        Book book = (Book) cbBook.getSelectedItem();
        if (nota == null) {
            nota = new Nota();
            nota.setCustomerName(custName);
            nota.setNotaDate(date);
            nota.setNotaNumber(notaNumber);
        }
        NotaDetail detail = new NotaDetail();
        detail.setId(idTmp);
        detail.setNota(nota);
        detail.setBook(book);
        notaDetails.add(detail);
        TableModel tableModel = new TrxTableModel(notaDetails);
        tblBook.setModel(tableModel);
        idTmp++;
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        try {
            notaRepository.create(nota);
            for (NotaDetail detail : notaDetails) {
                detail.setId(null);
                notaDetailRepository.create(detail);
            }
            notaDetails.clear();
            nota = new Nota();
            TableModel tableModel = new TrxTableModel(notaDetails);
            tblBook.setModel(tableModel);
            JOptionPane.showMessageDialog(this, "Data saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),
                    "validation error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        notaDetails.remove(row);
        TableModel tableModel = new TrxTableModel(notaDetails);
        tblBook.setModel(tableModel);
    }//GEN-LAST:event_btnDeleteActionPerformed


