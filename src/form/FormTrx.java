    void initData() {

        //book
        Iterable<Book> bookRows = bookRepository.findBookEntities();
        List<Book> books = new ArrayList<>();
        for (Book p : bookRows) {
            books.add(p);
        }
        DefaultComboBoxModel model = new DefaultComboBoxModel(books.toArray(new Book[books.size()]));
        cbBook.setModel(model);
    }

